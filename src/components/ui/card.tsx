import * as React from "react";
import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { elevated?: boolean }
>(({ className, elevated, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-border/80 bg-card/92 text-card-foreground shadow-elegant transition-shadow duration-300",
      elevated && "bg-card-elevated/95 shadow-card",
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

